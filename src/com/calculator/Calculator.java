package com.calculator;

public class Calculator {
	
	public static float add (float floatA, float floatB) {
		return (floatA+floatB);
	}
	
	public static float subtract(float floatA, float floatB) {
		return (floatA-floatB);
	}
	
	public static float multiply (float floatA, float floatB) {
		return (floatA*floatB);
	}
	
	public static float divide (float floatA, float floatB) {
		return (floatA/floatB);
	}

}
